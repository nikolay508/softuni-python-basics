yearly_tax = int(input())
first_sum_kicks = 0.4 * yearly_tax
sum_kicks = yearly_tax - first_sum_kicks
first_sum_equip = 0.2 * sum_kicks
sum_equip = sum_kicks - first_sum_equip
sum_ball = 0.25 * sum_equip
sum_accessories = 0.2 * sum_ball
print(yearly_tax + sum_kicks + sum_equip + sum_ball + sum_accessories)
