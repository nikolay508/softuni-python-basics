record_seconds = float(input()) 
meters_distance = float(input())  
seconds_per_meter = float(input()) 

time_to_swim_race =  meters_distance * seconds_per_meter 

syprotivlenie = meters_distance // 15 

time_to_swim_race = time_to_swim_race + (12.5 * syprotivlenie) 

if time_to_swim_race < record_seconds:
    print(f"Yes, he succeeded! The new world record is {time_to_swim_race:.2f} seconds.")
else:
    print(f"No, he failed! He was {time_to_swim_race - record_seconds:.2f} seconds slower.")
